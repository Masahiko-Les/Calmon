// ------------------------------------------------
// カルモンの状態
// ------------------------------------------------

/** カルモンが取り得る状態 */
export type CalmonState = 'normal' | 'hungry' | 'weak' | 'ghost';

// ------------------------------------------------
// 永続化データ (AsyncStorage に保存する構造)
// ------------------------------------------------

/**
 * AsyncStorage に保存するカルモンのセーブデータ。
 * アプリ再起動時にこの値を読み込んで続きから再開します。
 */
export interface CalmonSaveData {
  /** 空腹ゲージ (0〜100)。時間経過で减少します */
  hungerGauge: number;

  /** 所持エサ量。カロリー消費で増え、エサをあげると减ります */
  feedStock: number;

  /** カルモンの現在の状態 */
  state: CalmonState;

  /** 最終更新日時 (ISO 8601 文字列)。次回起動時の経過時間計算に使います */
  lastUpdatedAt: string;

  /**
   * 空腹ゲージが 0 になった日時 (ISO 8601 文字列)。
   * ghost 状態への移行タイミングを判定するため記録します。
   * ゲージが 0 でなければ null。
   */
  zeroHungerSince: string | null;

  /** 今日のカロリーをエサに変換した日付 (YYYY-MM-DD)。1日1回のみ加算するため使います */
  lastCalorieDate: string | null;

  /** 今日の消費カロリー (表示用)。毎日リセットされます */
  todayCalories: number;
}
